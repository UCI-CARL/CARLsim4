		ThreadStruct* args = (ThreadStruct*) arguments;
		//printf("\nThread ID: %lu and CPU: %d\n",pthread_self(), sched_getcpu());
		((SNN *)args->snn_pointer) -> doCurrentUpdateD1_CPU(args->netId);
